  `city_area` varchar(12) DEFAULT NULL COMMENT '所在城区',
  `area_name` varchar(12) DEFAULT NULL COMMENT '所在地区',
  `community_name` varchar(30) DEFAULT NULL COMMENT '所在小区',
  `community_meta` text COMMENT '该小区其他信息',
  `origin_title` varchar(80) DEFAULT NULL COMMENT '网页标题',
  `origin_url` varchar(80) DEFAULT NULL COMMENT '源网页',
